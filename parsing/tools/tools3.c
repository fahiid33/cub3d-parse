#include "../parse.h"

int	is_invalid(char c)
{
	if (c != '\0' && c != '1' && c != '0'
		&& c != ' ')
		return (1);
	return (0);
}

int	is_blank(char c)
{
	if (c == ' ' || c == '\t')
		return (1);
	return (0);
}
